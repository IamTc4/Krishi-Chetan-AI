let currentLang = 'en';

function setLanguage(lang) {
    currentLang = lang;
    document.querySelectorAll('[data-trn]').forEach(el => {
        const key = el.getAttribute('data-trn');
        if (translations[lang][key]) {
            el.innerText = translations[lang][key];
        }
    });

    // Update Voice Recognition Lang Code
    if (window.recognition) {
        if (lang === 'hi') window.recognition.lang = 'hi-IN';
        else if (lang === 'mr') window.recognition.lang = 'mr-IN';
        else window.recognition.lang = 'en-US';
    }

    // Trigger Data Refresh (Market/Officer modules)
    if (window.refreshData) window.refreshData();
}
